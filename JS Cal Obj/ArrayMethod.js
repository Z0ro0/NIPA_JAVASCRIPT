/* 지시사항 4번 내용을 코드로 작성해보세요. */
fruit.unshift("B");//맨 앞에 데이터 추가

/* 지시사항 5번 내용을 코드로 작성해보세요. */
document.write(fruit);

/* 지시사항 6번 내용을 코드로 작성해보세요. */
fruit.pop();//맨 끝 데이터 제거

/* 지시사항 7번 내용을 코드로 작성해보세요. */
fruit.shift();//맨 앞 데이터 제거

/* 지시사항 8번 내용을 코드로 작성해보세요. */
document.write(fruit);
